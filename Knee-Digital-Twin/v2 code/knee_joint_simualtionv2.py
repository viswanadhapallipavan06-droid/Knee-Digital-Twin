def generate_gait(t_end=10.0, n=600):
    """
    Piecewise gait model — one cycle T = 1.1 s:
      Stance (φ < 0.60):  θ = 10° · sin(π·φ / 0.60)   — weight-bearing flex
      Swing  (φ ≥ 0.60):  θ = 65° · sin(π·(φ−0.60)/0.40) — swing-through arc
    """
    T  = 1.1
    t  = np.linspace(0, t_end, n)
    th = np.zeros(n)
    for i, ti in enumerate(t):
        phi = (ti % T) / T
        th[i] = (10.0 * np.sin(np.pi * phi / 0.60) if phi < 0.60
                 else 65.0 * np.sin(np.pi * (phi - 0.60) / 0.40))
    dt    = t[1] - t[0]
    th_d  = np.gradient(th,   dt)
    th_dd = np.gradient(th_d, dt)
    return t, th, th_d, th_dd

def simulate_sensors(th, th_d, g):
    vmax = max(np.abs(th_d).max(), 1e-6)
    lf   = np.abs(np.sin(np.deg2rad(th))) + 0.15 * np.abs(th_d) / vmax
    return {'angle': th, 'vel': th_d, 'pressure': MASS * g * lf / 100.0}

def all_envs(th, th_d):
    return {e: simulate_sensors(th, th_d, g) for e, g in GRAVITY.items()}

def add_noise(s):
    n = len(s['angle'])
    return {**s,
            'angle_n'    : s['angle']    + np.random.normal(0, NOISE_ANG,   n),
            'vel_n'      : s['vel']      + np.random.normal(0, NOISE_VEL,   n),
            'pressure_n' : s['pressure'] + np.random.normal(0, NOISE_PRESS, n)}

def kalman(z, Q=2.0, R=16.0):
    """1-D Kalman filter: fuses noisy sensor with model prediction."""
    x = np.zeros(len(z)); P = 1.0; x[0] = z[0]
    for i in range(1, len(z)):
        Pp = P + Q; K = Pp / (Pp + R)
        x[i] = x[i-1] + K * (z[i] - x[i-1]); P = (1 - K) * Pp
    return x

def classify_risk(th, th_d, pressure):
    p_mean, p_std = pressure.mean(), pressure.std()
    hi  = (np.abs(th) > ANG_MAX) | (np.abs(th_d) > VEL_HIGH)
    me  = (~hi) & ((np.abs(th_d) > VEL_MED) | (pressure > p_mean + 2*p_std))
    lvl = np.where(hi, 'HIGH', np.where(me, 'MEDIUM', 'LOW'))
    rc  = np.where(hi, C['high'], np.where(me, C['medium'], C['low']))
    n   = len(lvl)
    print(f"\n  {'Risk':<8} {'Count':>6}  {'%':>6}")
    print(f"  {'─'*24}")
    for r, sym in [('HIGH','⚠'), ('MEDIUM','△'), ('LOW','✓')]:
        c = (lvl == r).sum()
        print(f"  {sym} {r:<8} {c:>5}  {100*c/n:>5.1f}%")
    return lvl, hi, me, rc

