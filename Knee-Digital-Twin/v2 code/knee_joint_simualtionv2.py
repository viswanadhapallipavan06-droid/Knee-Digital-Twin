def full_chain_frame(ax, ang_hip, ang_knee, ang_ankle, tilt, rc):
    """
    Draw a 4-segment leg: hip → thigh → knee → shin → ankle → foot.
    All angles measured from vertical.  Tilt rotates the foot segment.
    """
    ax.set_facecolor(C['bg'])
    ax.set_xlim(-2.0, 2.0); ax.set_ylim(-4.0, 0.6)
    ax.set_aspect('equal'); ax.axis('off')

    L = 1.0  # normalised segment length
    # Hip (origin at top)
    hx, hy = 0.0, 0.0
    # Thigh — angled by hip
    r_hip  = np.deg2rad(ang_hip)
    kx = hx + L*np.sin(r_hip); ky = hy - L*np.cos(r_hip)
    # Shin — angled by knee (relative to thigh direction)
    r_knee = np.deg2rad(ang_knee)
    sx = kx + L*np.sin(r_hip + r_knee); sy = ky - L*np.cos(r_hip + r_knee)
    # Foot — angled by ankle + tilt
    r_ankle = np.deg2rad(ang_ankle + tilt*0.3)
    fx = sx + 0.6*np.sin(r_hip+r_knee+r_ankle)
    fy = sy - 0.6*np.cos(r_hip+r_knee+r_ankle)

    # Draw segments
    segs = [([hx,kx],[hy,ky],C['hip'],  10,'Thigh'),
            ([kx,sx],[ky,sy],rc,          10,'Shin'),
            ([sx,fx],[sy,fy],C['ankle'],   8,'Foot')]
    for xs, ys, col, lw, lbl in segs:
        ax.plot(xs, ys, color=col, lw=lw, solid_capstyle='round', zorder=2)

    # Joints
    ax.scatter([hx,kx,sx,fx],[hy,ky,sy,fy],
               color='white', s=80, zorder=5)

    # Ground
    ax.axhline(fy, color='#444', lw=1.2, ls='--', alpha=.6)

    # Labels
    ax.text(0, 0.45, f'Hip {ang_hip:.1f}°',
            ha='center', color=C['hip'],   fontsize=8, fontweight='bold')
    ax.text(kx+.15, ky+.1, f'Knee {ang_knee:.1f}°',
            ha='left',   color=rc,          fontsize=8, fontweight='bold')
    ax.text(sx+.12, sy,    f'Ankle {ang_ankle:.1f}°',
            ha='left',   color=C['ankle'],  fontsize=8, fontweight='bold')
    ax.text(0, fy-.25, f'Tilt {tilt:.1f}°',
            ha='center', color=C['tilt'],   fontsize=8)

