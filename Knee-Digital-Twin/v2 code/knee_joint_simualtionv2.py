    g   = GRAVITY[environment]
    ft  = np.deg2rad(foot_tilt_deg)
    # Compute derived values
    F_e = body_weight * g * np.cos(ft)           # effective vertical
    F_l = body_weight * g * np.sin(ft)           # lateral
    # Example knee at 30° flex
    knee_ex  = 30.0
    F_j = np.sqrt(F_e**2 + F_l**2) * abs(np.sin(np.deg2rad(knee_ex)))
    # Approx EMG activation from force
    emg_val = min(F_j / (body_weight * g + 1e-6), 1.0)
    # Risk
    risk_v2 = ('HIGH'   if abs(foot_tilt_deg) > FOOT_TILT_MAX or F_j > 200 else
               'MEDIUM' if abs(foot_tilt_deg) > FOOT_TILT_MED or F_j > 100 else 'LOW')
    rc2 = C['high'] if risk_v2=='HIGH' else C['medium'] if risk_v2=='MEDIUM' else C['low']

    fig, axes = plt.subplots(1, 4, figsize=(20, 5))
    fig.patch.set_facecolor(C['panel'])
    fig.suptitle(
        f'VIZ 19 — V2 Interactive  |  Tilt={foot_tilt_deg}°  |  '
        f'Weight={body_weight} kg  |  {environment} (g={g} m/s²)  |  '
        f'Risk: {risk_v2}',
        color=rc2, fontsize=11, fontweight='bold')

    # Panel 1: full-chain anatomy snapshot
    ax = axes[0]; ax.set_facecolor(C['bg'])
    ax.set_xlim(-2.0,2.0); ax.set_ylim(-3.8,0.6)
    ax.set_aspect('equal'); ax.axis('off')
    full_chain_frame(ax, ang_hip=10.0, ang_knee=knee_ex,
                     ang_ankle=5.0+foot_tilt_deg*0.3,
                     tilt=foot_tilt_deg, rc=rc2)
    ax.set_title('Joint Chain', color='white', fontsize=9, fontweight='bold', pad=4)

    # Panel 2: foot tilt dial (bar)
    ax2 = axes[1]; ax2.set_facecolor(C['bg'])
    tilt_col = C['noisy'] if foot_tilt_deg < 0 else C['tilt']
    ax2.barh(['Foot Tilt [°]'], [abs(foot_tilt_deg)], color=tilt_col,
             height=.4, edgecolor='#30363D', lw=.5)
    ax2.set_xlim(0, 35)
    direction = 'Inversion' if foot_tilt_deg < 0 else 'Eversion'
    ax2.text(abs(foot_tilt_deg)+0.5, 0,
             f'{abs(foot_tilt_deg)}°  ({direction})',
             va='center', color='white', fontsize=10, fontweight='bold')
    _ax(ax2,'Foot Tilt')

    # Panel 3: force components bar
    ax3 = axes[2]; ax3.set_facecolor(C['bg'])
    cats3  = ['F_eff [N]','F_lateral [N]','F_joint [N]']
    vals3  = [F_e, abs(F_l), F_j]
    bcols3 = [C['true'], C['lateral'], rc2]
    bs3 = ax3.barh(cats3, vals3, color=bcols3, height=.4,
                   edgecolor='#30363D', lw=.5)
    ax3.set_xlim(0, max(vals3)*1.35+10)
    for b,v in zip(bs3,vals3):
        ax3.text(b.get_width()+2, b.get_y()+b.get_height()/2,
                 f'{v:.1f}', va='center', color='white',
                 fontsize=9, fontweight='bold')
    _ax(ax3,'Force Components')

    # Panel 4: gravity pressure comparison
    ax4 = axes[3]; ax4.set_facecolor(C['bg'])
    envl   = ['Earth','Moon','Mars']
    F_all  = [body_weight*GRAVITY[e]*np.cos(ft)*abs(np.sin(np.deg2rad(knee_ex)))
              for e in envl]
    bb = ax4.bar(envl, F_all,
                 color=[C['earth'],C['moon'],C['mars']],
                 width=.5, edgecolor='#30363D', lw=.5)
    for b,v in zip(bb,F_all):
        ax4.text(b.get_x()+b.get_width()/2, b.get_height()+0.5,
                 f'{v:.1f}', ha='center', color='white',
                 fontsize=9, fontweight='bold')
    bb[envl.index(environment)].set_edgecolor('white')
    bb[envl.index(environment)].set_linewidth(2.5)
    _ax(ax4,f'F_joint by Environment\n(knee={knee_ex}°)',yl='Force [N]')
    ax4.tick_params(axis='x',colors='#AAAAAA',labelsize=9)

    # EMG readout as text overlay
    fig.text(.5,.01,
             f'EMG activation ≈ {emg_val:.2f}  |  '
             f'F_effective={F_e:.1f} N  F_lateral={abs(F_l):.1f} N  F_joint={F_j:.1f} N',
             ha='center', color='#AAAAAA', fontsize=9)

    plt.tight_layout(rect=[0,0,1,.88]); show(fig)

